    private void setKdMatkul() throws SQLException{
        NilaiImplement nilai = new NilaiImplement();
        combomatkul.addItem("-=Pilih=-");
        ArrayList<String> urutanArr = nilai.viewKdMatkul();
        for(Object o : urutanArr){
            combomatkul.addItem(o);
        }
    }
    
    private void setNpm(String kd_kelas) throws SQLException{
        NilaiImplement nilai = new NilaiImplement();
        comboNpm.addItem("-=Pilih=-");
        ArrayList<String> urutanArr = nilai.viewNpm(kd_kelas);
        for(Object o : urutanArr){
            comboNpm.addItem(o);
        }
    }
    
    void loadData(String kode_matkul){
        try{
            record = nilai_im.getAll(kode_matkul);
        } catch (SQLException ex){
            Logger.getLogger(DataNilai.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    void isiTabel(){
        Object data[][]= new Object[record.size()][6];
        int x=0;
        for(Nilai nilai:record){
            data[x][0]=nilai.getNpm();
            data[x][1]=nilai.getNama();
            data[x][2]=nilai.getNilai_tugas();
            data[x][3]=nilai.getNilai_uts();
            data[x][4]=nilai.getNilai_uas();
            data[x][5]=nilai.getIndex();
            x++;
        }
        String judul[]={"NPM", "Nama Mahasiswa", "Nilai Tugas", "Nilai UTS", "Nilai UAS", "Index"};
        tblNilai.setModel(new DefaultTableModel(data, judul));
        jScrollPane1.setViewportView(tblNilai);
    }
    
    void isiText(){
        Nilai nilai = record.get(row);
        //txtKelas.setText(nilai.getId_kelas());
        comboNpm.setSelectedItem(nilai.getNpm());
        lblNama.setText(nilai.getNama());       
        String nilai_tugas = String.valueOf(nilai.getNilai_tugas());
        txtTugas.setText(nilai_tugas);
        String nilai_uts = String.valueOf(nilai.getNilai_uts());
        txtUts.setText(nilai_uts);
        String nilai_uas = String.valueOf(nilai.getNilai_uas());
        txtUas.setText(nilai_uts);
        String nilai_akhir = String.valueOf(nilai.getNilai_akhir());
        lblNa.setText(nilai_akhir);
        txtIndex.setText(nilai.getIndex());
    }

    void kosongkanText(){
        combomatkul.setSelectedIndex(0);
        txtKelas.setText("");
        comboNpm.setSelectedIndex(0);
        lblNama.setText("");
        txtTugas.setText("");
        txtUts.setText("");
        txtUas.setText("");
        lblNa.setText("");
        txtIndex.setText("");
    }
    
    void statusAwal(){
        kosongkanText();
        String kode = (String) combomatkul.getSelectedItem().toString();
        String kodeFix = kode.substring(0, 7);
        loadData(kodeFix);
        isiTabel();
    }
