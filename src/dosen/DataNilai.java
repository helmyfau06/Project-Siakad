    private void btnHitungNilaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungNilaiActionPerformed
        // TODO add your handling code here:
        int tugas = Integer.valueOf(txtTugas.getText());
        int uts = Integer.valueOf(txtUts.getText());
        int uas = Integer.valueOf(txtUas.getText());
        
        float na = (tugas+uts+uas)/3;
        
        if(na>=85){
            txtIndex.setText("A");
        } else if(na>=75){
            txtIndex.setText("B");
        } else if(na>=60){
            txtIndex.setText("C");
        } else if(na>=50){
            txtIndex.setText("D");
        } else{
            txtIndex.setText("E");
        }
        
        lblNa.setText(""+(int)na);
    }//GEN-LAST:event_btnHitungNilaiActionPerformed

