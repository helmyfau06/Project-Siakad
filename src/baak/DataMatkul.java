    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try {
            // TODO add your handling code here:
            Matkul matkul = new Matkul();
            matkul.setKode_matkul(txtKodeMatkul.getText());
            String kode = (String) dosen.getSelectedItem();
            String kodeFix = kode.substring(0, 10);
            matkul.setNidn(kodeFix);
            matkul.setId_kelas(kelas.getSelectedItem().toString());
            matkul.setNama_matkul(namaMatkul.getText());
            matkul.setSks(txtSks.getText());
            mtkl.insert(matkul);
            this.statusAwal();
            JOptionPane.showMessageDialog(this, "data tersimpan!");
        } catch (SQLException ex) {
            Logger.getLogger(DataMatkul.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnTambahActionPerformed

