    private void setKodeDosen() throws SQLException{
        MatkulImplement matkul = new MatkulImplement();
        dosen.addItem("Pilih Kode");
        ArrayList<String> urutanArr = matkul.viewKdDosen();
        for(Object o : urutanArr){
            dosen.addItem(o);
        }
    }
    
    private void setKodekelas() throws SQLException{
        MatkulImplement matkul = new MatkulImplement();
        kelas.addItem("Pilih Kode");
        ArrayList<String> urutanArr = matkul.viewKdKelas();
        for(Object o : urutanArr){
            kelas.addItem(o);
        }
    }
    
    void loadData(){
        try{
            record = mtkl.getAll();
        } catch (SQLException ex){
            Logger.getLogger(DataMatkul.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    void isiTabel(){
        Object data[][]= new Object[record.size()][5];
        int x=0;
        for(Matkul matkul:record){
            data[x][0]=matkul.getKode_matkul();
            data[x][1]=matkul.getNama_matkul();
            data[x][2]=matkul.getSks();
            data[x][3]=matkul.getNama_dosen();
            data[x][4]=matkul.getId_kelas();
            x++;
        }
        String judul[]={"Kode Matakuliah", "Nama Matakuliah", "SKS", "Nama Dosen", "Nama Kelas"};
        tblMatkul.setModel(new DefaultTableModel(data, judul));
        jScrollPane1.setViewportView(tblMatkul);
    }
    
    void isiText(){
        txtKodeMatkul.disable();
        Matkul matkul = record.get(row);
        txtKodeMatkul.setText(matkul.getKode_matkul());
        namaMatkul.setText(matkul.getNama_matkul());
        txtSks.setText(matkul.getSks());
        String nama_dosen = matkul.getNidn()+"-"+matkul.getNama_dosen();
        dosen.setSelectedItem(nama_dosen); 
        kelas.setSelectedItem(matkul.getId_kelas());
    }

    void kosongkanText(){
        try{
        txtKodeMatkul.enable();
        txtKodeMatkul.setText("");
        namaMatkul.setText("");
        txtSks.setText("");
        dosen.setSelectedIndex(0);
        kelas.setSelectedIndex(0);
        } catch(Exception e){          
        }
    }
    
    void statusAwal(){
        kosongkanText();
        loadData();
        isiTabel();
    }

