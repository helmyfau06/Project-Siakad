    private void setKodeJurusan() throws SQLException{
        MahasiswaImplement mhs_imp = new MahasiswaImplement();
        comboJurusan.addItem("-=Pilih=-");
        ArrayList<String> urutanArr = mhs_imp.viewKdJurusan();
        for(Object o : urutanArr){
            comboJurusan.addItem(o);
        }
    }
    
    private void setKodeKelas(String kd_jurusan) throws SQLException{
        MahasiswaImplement mhs_imp = new MahasiswaImplement();
        comboKelas.addItem("-=Pilih=-");
        ArrayList<String> urutanArr = mhs_imp.viewKdKelas(kd_jurusan);
        for(Object o : urutanArr){
            comboKelas.addItem(o);
        }
    }
    
    void loadData(){
        try{
            record = mhs.getAll();
        } catch (SQLException ex){
            Logger.getLogger(DataMahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    void isiTabel(){
        Object data[][]= new Object[record.size()][7];
        int x=0;
        for(Mahasiswa mahasiswa:record){
            data[x][0]=mahasiswa.getNpm();
            data[x][1]=mahasiswa.getNama_mhs();
            data[x][2]=mahasiswa.getId_jurusan();
            data[x][3]=mahasiswa.getId_kelas();
            data[x][4]=mahasiswa.getJk();
            data[x][5]=mahasiswa.getTgl_lahir();
            data[x][6]=mahasiswa.getAlamat();
            x++;
        }
        String judul[]={"NPM", "Nama Mahasiswa", "Jurusan", "Kelas", "Jenis Kelamin", "Tanggal Lahir", "Alamat"};
        tblMhs.setModel(new DefaultTableModel(data, judul));
        jScrollPane1.setViewportView(tblMhs);
    }
    
    void isiText(){
        txtNpm.disable();
        Mahasiswa mahasiswa = record.get(row);
        txtNpm.setText(mahasiswa.getNpm());
        txtNama.setText(mahasiswa.getNama_mhs());
        txtTglLahir.setText(mahasiswa.getTgl_lahir());
        txtAlamat.setText(mahasiswa.getAlamat());
        comboJurusan.setSelectedItem(mahasiswa.getId_jurusan());
        radioPerempuan.setSelected(mahasiswa.getJk().equals("Perempuan"));
        radioLaki.setSelected(mahasiswa.getJk().equals("Laki-laki"));
        comboKelas.setSelectedItem(mahasiswa.getId_kelas());
    }

    void kosongkanText(){
        try{
            txtNpm.enable();
            txtNpm.setText("");
            txtNama.setText("");
            txtTglLahir.setText("");
            txtAlamat.setText("");
            comboJurusan.setSelectedIndex(0);
            buttonGroup1.clearSelection();
            comboKelas.setSelectedIndex(0);
        } catch(Exception e){
        }
        
    }
    
    void statusAwal(){       
        kosongkanText();
        loadData();
        isiTabel();             
    }

