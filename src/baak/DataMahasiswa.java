    private void btnTambah1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambah1ActionPerformed
        try {
            // TODO add your handling code here:
            Mahasiswa mahasiswa = new Mahasiswa();
            mahasiswa.setNpm(txtNpm.getText());
            mahasiswa.setNama_mhs(txtNama.getText());
            mahasiswa.setJk(radioPerempuan.isSelected()? radioPerempuan.getText() : radioLaki.getText());
            mahasiswa.setTgl_lahir(txtTglLahir.getText());
            mahasiswa.setAlamat(txtAlamat.getText());
            mahasiswa.setId_kelas(comboKelas.getSelectedItem().toString());
            mhs.insert(mahasiswa);
            this.statusAwal();
            JOptionPane.showMessageDialog(this, "data tersimpan!");
        } catch (SQLException ex) {
            Logger.getLogger(DataMahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnTambah1ActionPerformed

